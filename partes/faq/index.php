      <li itemscope itemprop="itemListElement" itemtype="https://schema.org/Question" class="pergunta">
        <h4 itemprop="name">Se eu estiver em trânsito fora da minha cidade e precisar de um atendimento de urgência o que eu faço?</h4>
        <div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer" class="resposta">
          <p itemprop="text">
            Se você estiver fora da sua cidade e precisar de <strong>atendimento de urgência</strong>,
            o ideal é <strong>entrar em contato imediatamente com o SAC do seu convênio</strong>.
            Eles podem fornecer informações precisas sobre os <strong>prontos-socorros e hospitais credenciados na região onde você se encontra</strong>,
            garantindo que você receba atendimento rápido e seguro. Dessa forma, você tem a <strong>tranquilidade de contar com suporte confiável em qualquer lugar</strong>,
            evitando atrasos ou complicações em situações de emergência.
          </p>
        </div>
      </li>
