      <!---------------------------faq  6-------------------------------------------------------------------------------------------------------------------->
      <li itemscope itemprop="itemListElement" itemtype="https://schema.org/Question" class="pergunta">
        <h4 itemprop="name">Quanto tempo de carência tem para eu tratar doenças pré existentes?</h4>
        <div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer" class="resposta">
          <p itemprop="text">
            O período de carência para tratamento de <strong>doenças pré-existentes geralmente é de 24 meses após a assinatura do 
            contrato do plano de saúde</strong>. Durante esse período, alguns procedimentos relacionados à doença podem <strong>não estar 
            totalmente cobertos</strong>, dependendo das regras do convênio. É fundamental consultar a administração do seu plano para entender
             exatamente quais tratamentos estão incluídos e como funcionam as regras de cobertura. Assim, você garante <strong>planejamento 
             e segurança no cuidado com a sua saúde</strong>, evitando surpresas ou limitações inesperadas.
          </p>
        </div>
      </li>
      <!----------------------------------------------------------------------------------------------------------------------------------------------------->




      <!---------------------------faq  7-------------------------------------------------------------------------------------------------------------------->
      <li itemscope itemprop="itemListElement" itemtype="https://schema.org/Question" class="pergunta">
        <h4 itemprop="name">No caso de um acidente de trânsito, o plano de saúde envia ambulância para me socorrer no local do acidente</h4>
        <div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer" class="resposta">
          <p itemprop="text">
           Não. Em casos de <strong>acidentes de trânsito</strong>, o <strong>socorro no local do acidente é realizado pelo SUS (Sistema Único de Saúde)</strong>, 
           que disponibiliza ambulâncias e atendimento de emergência imediato. O plano de saúde passa a atuar <strong>a partir do momento 
           em que você é levado a um hospital credenciado</strong>, oferecendo cobertura para internações, exames, cirurgias e tratamentos necessários. 
           É importante conhecer essas regras para <strong>planejar seu atendimento de urgência com segurança</strong>, garantindo que você receba cuidado rápido 
           e adequado quando mais precisa.
          </p>
        </div>
      </li>
