      <!---------------------------faq  5-------------------------------------------------------------------------------------------------------------------->
      <li itemscope itemprop="itemListElement" itemtype="https://schema.org/Question" class="pergunta">
        <h4 itemprop="name">Quais os procedimentos que eu não tenho direito?</h4>
        <div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer" class="resposta">
          <p itemprop="text">
            Você <strong>não tem direito a procedimentos que não são reconhecidos pela ANS</strong> (Agência Nacional de Saúde Suplementar),
            bem como a <strong>tratamentos alternativos ou não convencionais</strong> que não estejam incluídos na cobertura do seu plano de saúde.
            É importante verificar sempre a lista de procedimentos cobertos pelo convênio, garantindo clareza sobre o que está incluído
            e evitando surpresas na hora do atendimento. Com essa informação, você consegue <strong>planejar seu cuidado de saúde com segurança
              e responsabilidade</strong>, sabendo exatamente quais serviços estão disponíveis.
          </p>
        </div>
      </li>
