      <!---------------------------faq  3-------------------------------------------------------------------------------------------------------------------->
      <li itemscope itemprop="itemListElement" itemtype="https://schema.org/Question" class="pergunta">
        <h4 itemprop="name">Se eu precisar de atendimento para algum tratamento que está em carência, tem alguma negociação para eu conseguir ser atendido?</h4>
        <div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer" class="resposta">
          <p itemprop="text">
            Isso depende de cada convênio. O ideal é <strong>entrar em contato com a administração do seu plano de saúde</strong> para se informar
            sobre as possibilidades de atendimento durante o período de carência. Alguns convênios podem oferecer alternativas,
            como <strong>antecipação parcial de cobertura ou programas de autorização</strong> especial, dependendo do caso.
            Ao se informar diretamente com a operadora,
            você garante clareza sobre seus direitos e opções, evitando surpresas e podendo planejar o cuidado com a sua saúde de forma segura e responsável.
          </p>
        </div>
      </li>
