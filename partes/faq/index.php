            <strong>internações hospitalares</strong> e cirurgias, garantindo atendimento de qualidade quando você mais precisa.
            Além disso, você conta com cobertura para <strong>situações de urgência e emergência</strong>, assegurando cuidado rápido
            e eficiente em qualquer imprevisto de saúde.
            Com um plano bem estruturado, você pode ter <strong>tranquilidade e segurança para cuidar da sua saúde e da sua família</strong>
            sem preocupações com gastos inesperados.
          </p>
        </div>
